class Link {
  final int id;
  final String title;
  final String link;
  final String username;
  final int isActive;
  final int userId;
  final String createdAt;
  final String updatedAt;

  Link({
    required this.id,
    required this.title,
    required this.link,
    required this.username,
    required this.isActive,
    required this.userId,
    required this.createdAt,
    required this.updatedAt,
  });
}
